#define ME_SCRIPT_WRAPPER(NAME) ME_SCRIPT_WRAPPER_RA(GEInt, NAME, GEInt)
#define ME_SCRIPT_WRAPPER_A(NAME, ARG_TYPE) ME_SCRIPT_WRAPPER_RA(GEInt, NAME, ARG_TYPE)
#define ME_SCRIPT_WRAPPER_R(RET_TYPE, NAME) ME_SCRIPT_WRAPPER_RA(RET_TYPE, NAME, GEInt)
#define ME_SCRIPT_WRAPPER_RA(RET_TYPE, NAME, ARG_TYPE)                                                                 \
    RET_TYPE inline NAME(Entity &a_pSelfEntity, Entity &a_pOtherEntity = None,                                         \
                         ARG_TYPE a_iArgs = static_cast<ARG_TYPE>(0))                                                  \
    {                                                                                                                  \
        return static_cast<RET_TYPE>(FindModule<gCScriptAdmin>()->CallScriptFromScript(                                \
            #NAME, &a_pSelfEntity, &a_pOtherEntity, static_cast<GEInt>(a_iArgs)));                                     \
    }

#define ME_SCRIPT_WRAPPER_B(NAME) ME_SCRIPT_WRAPPER_BA(NAME, GEInt)
#define ME_SCRIPT_WRAPPER_BA(NAME, ARG_TYPE)                                                                           \
    GEBool inline NAME(Entity &a_pSelfEntity, Entity &a_pOtherEntity = None,                                           \
                       ARG_TYPE a_iArgs = static_cast<ARG_TYPE>(0))                                                    \
    {                                                                                                                  \
        return FindModule<gCScriptAdmin>()->CallScriptFromScript(#NAME, &a_pSelfEntity, &a_pOtherEntity,               \
                                                                 static_cast<GEInt>(a_iArgs))                          \
            == 1;                                                                                                      \
    }

